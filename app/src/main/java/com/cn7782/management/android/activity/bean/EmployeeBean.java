package com.cn7782.management.android.activity.bean;

import java.io.Serializable;

public class EmployeeBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5006092988095239325L;
	
	private String contactId;//通讯录id
	private Integer officeId;//部门id
	private String officeName;//部门名称
	private String empName;
	private String email;
	private String fax;
	private String homeAddr;
	private String mobile;
	private String mobile2;
	private String officeAddr;
	private String remark;
	private String officeTel;
	public EmployeeBean(){
		
	}
	public EmployeeBean(String contactId,Integer officeId,String officeName,
			String empName,String email,String fax,String homeAddr,String mobile,
			String mobile2,String officeAddr,String remark,String officeTel){
		this.contactId = contactId;
		this.officeId = officeId;
		this.officeName = officeName;
		this.empName = empName;
		this.email = email;
		this.fax = fax;
		this.homeAddr = homeAddr;
		this.mobile = mobile;
		this.mobile2 = mobile2;
		this.officeAddr = officeAddr;
		this.remark = remark;
		this.officeTel = officeTel;
	}
	public String getContactId() {
		return contactId;
	}
	public void setContactId(String contactId) {
		this.contactId = contactId;
	}
	public long getOfficeId() {
		return officeId;
	}
	public void setOfficeId(Integer officeId) {
		this.officeId = officeId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getOfficeName() {
		return officeName;
	}
	public void setOfficeName(String officeName) {
		this.officeName = officeName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getHomeAddr() {
		return homeAddr;
	}
	public void setHomeAddr(String homeAddr) {
		this.homeAddr = homeAddr;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getMobile2() {
		return mobile2;
	}
	public void setMobile2(String mobile2) {
		this.mobile2 = mobile2;
	}
	public String getOfficeAddr() {
		return officeAddr;
	}
	public void setOfficeAddr(String officeAddr) {
		this.officeAddr = officeAddr;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getOfficeTel() {
		return officeTel;
	}
	public void setOfficeTel(String officeTel) {
		this.officeTel = officeTel;
	}
	
	
}
