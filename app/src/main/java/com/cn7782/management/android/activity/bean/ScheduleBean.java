package com.cn7782.management.android.activity.bean;

import java.io.Serializable;

public class ScheduleBean implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -2261949993801713381L;
	private String scheduleId;
	private String title;
	private String schdate;
	private String begtime;
	private String endtime;
	private String content;
	private String noticetime;
	private String status;
	private String develop_user;
	private String develop_time;
	private String jobType;
	
	public ScheduleBean(){
		
	}
	public ScheduleBean(String scheduleId,String title,String content,String begtime,
			String endtime,String schdate,String noticetime,String status,
			String jobType){
		
		this.scheduleId = scheduleId;
		this.schdate = schdate;
		this.begtime = begtime;
		this.endtime = endtime;
		this.content = content;
		this.noticetime = noticetime;
	}
	public String getScheduleId() {
		return scheduleId;
	}
	public void setScheduleId(String scheduleId) {
		this.scheduleId = scheduleId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSchdate() {
		return schdate;
	}
	public void setSchdate(String schdate) {
		this.schdate = schdate;
	}
	public String getBegtime() {
		return begtime;
	}
	public void setBegtime(String begtime) {
		this.begtime = begtime;
	}
	public String getEndtime() {
		return endtime;
	}
	public void setEndtime(String endtime) {
		this.endtime = endtime;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getNoticetime() {
		return noticetime;
	}
	public void setNoticetime(String noticetime) {
		this.noticetime = noticetime;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDevelop_user() {
		return develop_user;
	}
	public void setDevelop_user(String develop_user) {
		this.develop_user = develop_user;
	}
	public String getDevelop_time() {
		return develop_time;
	}
	public void setDevelop_time(String develop_time) {
		this.develop_time = develop_time;
	}
	public String getJobType() {
		return jobType;
	}
	public void setJobType(String jobType) {
		this.jobType = jobType;
	}
	
}
